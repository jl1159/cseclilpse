package coreservlets.eclipse.editor.java;

public class MouselessJavaEditorPuzzle {

	// ***Without using your mouse***, make the necessary changes in the most
	// keyboard-efficient manner possible. All text should
	// display as stated. Some useful key bindings are provided
	// below.
	public static void main(String[] args) {

		System.out.println("i should display in uppercase");

		System.out.println("I SHOULD DISPLAY IN LOWERCASE");

		System.out.println("Delete me! I shouldn't display at all!");

		System.out.println("Comment me out! I shouldn't display at all!");
		
		System.out.println("I should display three times in a row!");
		
		System.out.println("Oh_no,_all_of_my_underscores_should_be_spaces!_Fix_me!");

		System.out.println("My numbers should display in order! THREE FIVE ONE FOUR TWO");

		System.out.println("  | |    ___| |__   ___  _   _| | __| |   __| |_ ___ _ __ | | __ _ _   _    __ _ ___     /  \\  | (___ | |      | |   | |  "); // ASCII line 3
		System.out.println("                                                    | |             __/ |                                                 "); // ASCII line 7
	  System.out.println("|_   _|     | |               | |   | |     | (_)         | |                             /\\    / ____|/ ____|_   _|_   _|"); // ASCII line 2
	  System.out.println(" _____       _                 _     _       _ _           _                                     _____  _____ _____ _____ "); // ASCII line 1
	  System.out.println(" _| |_  \\__ | | | | (_) | |_| | | (_| | | (_| | \\__ | |_) | | (_| | |_| | | (_| \\__ \\  / ____ \\ ____) | |____ _| |_ _| |_ "); // ASCII line 5
		System.out.println("                                                    |_|            |___/                                                  "); // ASCII line 8
		System.out.println("|_____| |___|_| |_|\\___/ \\__,_|_|\\__,_|  \\__,_|_|___| .__/|_|\\__,_|\\__, |  \\__,_|___/ /_/    \\_|_____/ \\_____|_____|_____|"); // ASCII line 6
		System.out.println("  | |   / __| '_ \\ / _ \\| | | | |/ _` |  / _` | / __| '_ \\| |/ _` | | | |  / _` / __|   / /\\ \\  \\___ \\| |      | |   | |  "); // ASCII line 4

		// Reminder - some useful key bindings:
		// Cut/Copy/Paste: Ctrl + X/C/V
		// Select word: Ctrl + Shft + Left/Right
		// Find/replace: Ctrl + F
		// Undo/redo: Ctrl + Z/Y
		// Go to line: Ctrl + L
		// Delete line: Ctrl + D
		// Comment line: Ctrl + / Ctrl + Shft + /
		// Toggle window maximize: Ctrl + M
		// Move line: Alt + Up/Down
		// To uppercase: Ctrl + Shft + X
		// To lowercase: Ctrl + Shft + Y

	}

}
